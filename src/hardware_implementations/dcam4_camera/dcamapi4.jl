function alloctext!(obj::DCAMDEV_STRING, maxlen)
    textbuf = Vector{Cchar}(undef, maxlen)
    obj.text = Base.unsafe_convert(Ptr{Cchar}, Base.unsafe_convert(UInt, Base.pointer(textbuf)))
    obj.textbytes = sizeof(textbuf)
end

